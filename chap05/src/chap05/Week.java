package chap05;

// 열거 타입
// 열거 타입은 한정된 값만을 갖는 데이터 타입
// 열거 타입을 선언하기 위해서는 먼저 열거 타입의 이름을 정하고
// 열거 타입 이름으로 소스 파일을 생성해야 한다.

// 이름은 관례적으로 첫 문자를 대문자, 나머지는 소문자로 구성
// ex) public enum 열거타입이름 {...}
// 열거 타입을 선언하고 열거 상수를 선언한다.
// 상수는 관례적으로 대문자로 작성
// ex) public enum Week {MONDAY, TUSEDAY, WEDNESDAY, ...}

public enum Week {
	MONDAY,
	TUESDAY,
	WEDNESDAY,
	THURSDAY,
	FRIDAY,
	SATURDAY,
	SUNDAY
	
	

	
}

package sec06.exam01;

public class IntExample {
	public static void main(String[] args) {
		// 8진수는 앞에 0을, 16진수는 앞에 0x를 붙여줘야한다.
		int number = 10;
		int octNumber = 012;
		int hexNumber = 0xA;
		
		
		System.out.println(number);
		System.out.println(octNumber);
		System.out.println(hexNumber);
		
		
		
		
		
	}
}

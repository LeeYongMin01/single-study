package scene.c5;

// <7.5> final 클래스와 final 메소드

public class Concept {
	
	// final 키워드는 클래스, 필드, 메소드 선언 시에 사용 가능
	// 수정될 수 없음을 뜻한다.
	
// <7.5.1> 상속할 수 없는 final 클래스
	// 클래스 앞에 final을 붙이면 최종적인 클래스가 되므로 상속이 불가능
	// 대표적인 final 클래스가 String. String 클래스는 자식 클래스 생성이 불가능
	
// <7.5.2> 오버라이딩할 수 없는 final 메소드
	// 메소드 앞에 final을 붙이면 최정적인 메소드가 되므로 오버라이딩이 불가능
	// 즉, 부모로부터 상속받은 메소드를 재정의할 수 없다.
	


	
	
	
	

}
